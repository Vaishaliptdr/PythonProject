# List is represented by []
# We can store list in another list


a=[1,2,3,4,4,1.4,3.5,'Ganesh']
print(a)

# List index starts with 0
print(a.index(1))   #Find the index of mentioned element
print(a.index(4))

print(a.reverse())   # Reverse the list elements
print(a)
print(a.reverse())

print(a.pop())       #by default pop outs last element of the list
print(a.pop(5))

print(a.append(89))  #Appends entered value in list
print(a)

print(a.count(4))    #Counts the mentioned elements from list

print(a.copy())

